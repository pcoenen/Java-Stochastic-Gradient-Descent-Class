

public class TrainingExample {
	public TrainingExample(String X, String Y){
		setX(X);
		setY(Y);
	}
	public String getX() {
		return X;
	}
	private void setX(String x) {
		X = x;
	}
	private String X;
	
	public String getY() {
		return Y;
	}
	private void setY(String y) {
		Y = y;
	}
	
	private String Y;

}
